    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        String bloodGroup = (String) jComboBox1.getSelectedItem(); // Get selected blood group
        String unit = jTextField1.getText(); // Get units from text field
        if (unit.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter units");
            return;
        }
        int unit1 = Integer.parseInt(unit); // Convert units to integer

        try {
            Connection con = ConnectionProvider.getConnection();
            Statement st = con.createStatement();

            // Use the correct column name: bloodGoup (as per your database)
            String query = "update stock set units = units + " + unit1 + " where bloodGroup = '" + bloodGroup + "'";
            System.out.println("Executing Query: " + query); // Print the query for debugging

            st.executeUpdate(query); // Execute the query
            JOptionPane.showMessageDialog(null, "Successfully updated"); // Show success message
            setVisible(false); // Hide current window
            new stockIncrease().setVisible(true); // Open new window
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage()); // Show error message
        }
    }// GEN-LAST:event_jButton1ActionPerformed

